def _make_article(raw: dict, category: str, idx: int) -> dict:
    """원본 딕셔너리에 메타데이터를 추가해 완성된 기사 딕셔너리 반환."""
    source_pool = SOURCES.get(category, SOURCES["전체"])
    days_ago = random.randint(0, 2)
    hours_ago = random.randint(0, 23)
    pub_dt = datetime.now() - timedelta(days=days_ago, hours=hours_ago)

    return {
        **raw,
        "category": category,
        "source": random.choice(source_pool),
        "link": "#",                             # 실제 API 연결 시 교체
        "date": pub_dt.strftime("%Y.%m.%d %H:%M"),
        "views": random.randint(1_000, 120_000),
        "idx": idx,
    }


def get_dummy_news(category: str = "전체", query: str = "") -> list[dict]:
    """
    카테고리와 검색어에 맞는 더미 뉴스 리스트를 반환.

    Parameters
    ----------
    category : str
        "전체" | "사회" | "경제" | "스포츠" | "연예"
    query : str
        검색어 (제목·설명·요약에 포함된 기사만 필터링)

    Returns
    -------
    list[dict]  정렬: 최신순 (랜덤 date 기준)
    """
    if category == "전체":
        # 전체 카테고리: 모든 카테고리 기사를 합쳐서 반환
        raw_list = []
        for cat, items in _RAW.items():
            for item in items:
                raw_list.append((cat, item))
        random.shuffle(raw_list)
    else:
        raw_list = [(category, item) for item in _RAW.get(category, [])]

    articles = [_make_article(raw, cat, i) for i, (cat, raw) in enumerate(raw_list)]

    # 검색어 필터
    if query.strip():
        q = query.strip().lower()
        articles = [
            a for a in articles
            if q in a["title"].lower()
            or q in a["desc"].lower()
            or q in a["summary"].lower()
        ]

    # 최신순 정렬
    articles.sort(key=lambda x: x["date"], reverse=True)
    return articles


